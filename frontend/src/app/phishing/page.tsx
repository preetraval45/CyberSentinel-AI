import { Mail, AlertTriangle, Shield, Zap } from 'lucide-react'
import Scene3D from '../../components/3d/Scene3D'
import PhishingHook from '../../components/3d/PhishingHook'
import FakeWebsite from '../../components/3d/FakeWebsite'
